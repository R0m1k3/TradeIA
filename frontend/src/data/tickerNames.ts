/**
 * Retourne le nom d'entreprise (ex: "Apple") pour un ticker (ex: "AAPL").
 * Si le ticker n'est pas connu, retourne le ticker brut.
 */
export function getTickerName(ticker: string): string {
  return TICKER_NAMES[ticker] || ticker;
}

/**
 * Indique si on a un nom mappé pour ce ticker.
 * Utile pour décider d'afficher ou non le nom à côté du code.
 */
export function hasTickerName(ticker: string): boolean {
  return ticker in TICKER_NAMES && TICKER_NAMES[ticker] !== ticker;
}
